exports.filterVoucher = async (req, res) => {
  let totalCharge = 0
  let totalCredit = 0
  let totalCashdown = 0
  console.log(totalCharge, totalCredit, totalCashdown)
  let { start, end } = req.body;
  let query = { isDeleted: false }
  if (start && end) query.date = { $gte: start, $lte: end }
  const result = await Voucher.find(query).populate([
    { path: 'relatedPatient' },
    { path: 'referDoctor' },
    { path: 'testSelection.name' }
  ])
  result.map((e, i) => {
    totalCharge = e.totalCharge + totalCharge
    totalCredit = e.creditAmount + totalCredit
  })
  totalCashdown = totalCharge - totalCredit
  return res.status(200).send({ success: true, data: result, totalVoucher: totalCharge, totalCredit: totalCredit * (-1), totalCashdown: totalCashdown })
}

exports.getVouchersWithDoctorIDandTime = async (req, res) => {
  const { referDoctor, fromDate, toDate } = req.body;

  const result = await Voucher.aggregate([
    {
      $match: {
        referDoctor: referDoctor,
        isDeleted: false,
        createdAt: {
          $gte: new Date(fromDate),
          $lte: new Date(toDate)
        }
      }
    },
    {
      $group: {
        _id: null,
        totalAmount: { $sum: "$totalCharge" },
        vouchers: { $push: "$$ROOT" },
        count: { $sum: 1 }
      }
    },
    {
      $project: {
        _id: 0,
        totalAmount: 1,
        count: 1,
        data: "$vouchers"
      }
    }
  ]);
  if (result.length === 0) {
    return res.status(500).json({ success: false, message: 'No Record Found' });
  }

  const { totalAmount, count, data } = result[0];
  return res.status(200).json({
    success: true,
    count: count,
    data: data,
    totalAmount: totalAmount
  });
};
