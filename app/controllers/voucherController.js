exports.voucherPayment = async (req, res) => {
  try {
    let isPaid = false
    let { repayAmount, voucherID, repayDate, creditRemark } = req.body;
    const creditResult = await Voucher.find({ _id: voucherID }).populate('relatedPatient').populate('referDoctor').populate('testSelection.name')
    let leftover = creditResult[0].creditAmount - repayAmount
    if (leftover <= 0) {
      isPaid = true
    }
    if (isPaid === true) repayAmount = creditResult[0].creditAmount + creditResult[0].repayAmount
    const result = await Voucher.updateOne(
      { "_id": req.body.voucherID, },
      { $inc: { creditAmount: -repayAmount }, isPaid: isPaid, updatedAt: repayDate, creditRemark: creditRemark },
      { new: true }
    ).populate('relatedPatient').populate('referDoctor').populate('testSelection.name')
    if (result.length === 0) return res.status(404).send({ error: true, message: 'No Record Found!' })
    return res.status(200).send({ success: true, data: result })
  } catch (error) {
    return res.status(500).send({ "error": true, "message": error.message })
  }
}

exports.getRelatedVouchers = async (req, res) => {
  try {
    const { relatedPatient, referDoctor, date, status, start, end } = req.body;
    const query = { isDeleted: false };
    if (relatedPatient) query.relatedPatient = relatedPatient;
    if (referDoctor) query.referDoctor = referDoctor;
    if (date) query.date = date;
    if (status) query.status = status;
    if (start && end) query.date = { $gte: start, $lte: end }
    const result = await Voucher.find(query)
      .populate([
        { path: "relatedPatient" },
        { path: "referDoctor" },
        { path: "testSelection.name" },
      ])
      .exec();
    if (!result.length) {
      return res.status(404).json({
        success: false,
        message: "No related vouchers found.",
      });
    }
    return res.status(200).json({
      success: true,
      data: result,
    });
  } catch (err) {
    console.error(err);
    return res.status(500).json({
      success: false,
      message: "An error occurred while fetching related vouchers.",
    });
  }
};

